import random
import math

print("===== PYTHON MODULES PRACTICE =====")

# Random Module
random_number = random.randint(1, 100)

print("\nRandom Number:", random_number)

# Math Module
number = int(input("\nEnter a number: "))

print("\nSquare Root :", math.sqrt(number))
print("Power (Square) :", math.pow(number, 2))
print("Ceiling Value :", math.ceil(number))
print("Floor Value :", math.floor(number))

print("\n===== PROGRAM COMPLETED =====")
